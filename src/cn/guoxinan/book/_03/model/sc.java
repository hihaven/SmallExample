package cn.guoxinan.book._03.model;

public class sc {
	private String xuehao;
	private String kechenghao;
	private int chengji;
	public String getXuehao() {
		return xuehao;
	}
	public void setXuehao(String xuehao) {
		this.xuehao = xuehao;
	}
	public String getKechenghao() {
		return kechenghao;
	}
	public void setKechenghao(String kechenghao) {
		this.kechenghao = kechenghao;
	}
	public int getChengji() {
		return chengji;
	}
	public void setChengji(int chengji) {
		this.chengji = chengji;
	}
	@Override
	public String toString() {
		return "sc [xuehao=" + xuehao + ", kechenghao=" + kechenghao + ", chengji=" + chengji + "]";
	}
	
}
